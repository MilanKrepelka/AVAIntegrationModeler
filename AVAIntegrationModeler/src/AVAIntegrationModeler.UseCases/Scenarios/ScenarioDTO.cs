using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using AVAIntegrationModeler.Core.ValueObjects;

namespace AVAIntegrationModeler.UseCases.Scenarios;
/// <summary>
/// DTO pro integrační scénář.
/// </summary>
public record ScenarioDTO
{
  /// <summary>
  /// Jedinečný identifikátor scénáře.
  /// </summary>
  public Guid Id { get; init; }

  /// <summary>
  /// Kód scénáře, který slouží k jeho jednoznačné identifikaci v rámci systému.
  /// </summary>
  public string Code { get; init; } = string.Empty;

  /// <summary>
  /// Lokalizovaný název scénáře (např. v češtině a angličtině).
  /// </summary>
  public LocalizedValue Name { get; init; } = new();

  /// <summary>
  /// Lokalizovaný popis scénáře (např. v češtině a angličtině).
  /// </summary>
  public LocalizedValue Description { get; init; } = new();

  /// <summary>
  /// Identifikátor vstupní feature, která je součástí scénáře.
  /// </summary>
  public Guid? InputFeatureId { get; init; }

  /// <summary>
  /// Identifikátor výstupní feature, která je součástí scénáře.
  /// </summary>
  public Guid? OutputFeatureId { get; init; }
}

